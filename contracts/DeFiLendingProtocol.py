    def _recalculate_global_score(self):
        """
        Iterates over recent approved proposals to calculate a rolling 
        weighted average of the systemic evaluation score.
        """
        total_score = 0
        count = 0
        
        # Iterate backwards over the array
        idx = int(self.state.app_counter)
        while idx > 0 and count < MAX_SYSTEM_METRICS:
            aid = f"APP-{idx}"
            if aid in self.applications:
                a = self.applications[aid]
                if a.status == "APPROVE":
                    total_score += int(a.score)
                    count += 1
            idx -= 1
            
        if count > 0:
            self.state.global_score_index_bps = u256(total_score // count)
        else:
            self.state.global_score_index_bps = u256(0)


