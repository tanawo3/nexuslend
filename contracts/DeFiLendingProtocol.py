ERROR_EXPECTED = "[EXPECTED]"    # Deterministic business-logic violation
ERROR_EXTERNAL = "[EXTERNAL]"    # Deterministic external oracle failure
ERROR_TRANSIENT = "[TRANSIENT]"  # Non-deterministic network partition
ERROR_LLM = "[LLM_ERROR]"        # Consensus breakdown or prompt hallucination

@allow_storage
@dataclass
class Pool:
    """
    Core data structure representing a multi-lingual lending pool.
    """
    pool_id: str
    amount: u256
    conditions: str
    status: str
    created_at: u256

@allow_storage
@dataclass
class Application:
    """
    Core data structure representing a loan application in any language.
    By utilizing strict dataclasses, we guarantee memory layout determinism
    across all nodes running GenVM, avoiding float/dict instantiation drifts.
    
    Attributes:
        app_id (str): UUID assigned internally.
        pool_id (str): Target pool reference.
        applicant (str): Sanitized identifier of the borrower.
        pitch (str): The borrower's business pitch (original language).
        status (str): "PENDING_EVALUATION" | "APPROVE" | "REJECT" | "REVOKED".
        translated_pitch (str): English translation generated by AI.
        score (u256): Dynamic evaluation score in basis points (0-10000).
        ai_notes (str): Immutable summary generated by the AI leader.
        created_at (u256): Timestamp proxy (block epoch).
        last_updated (u256): Last state transition block.
    """
    app_id: str
    pool_id: str
    applicant: str
    pitch: str
    status: str
    translated_pitch: str
    score: u256
    ai_notes: str
    collateral: u256
    debt: u256
    created_at: u256
    last_updated: u256

@allow_storage
@dataclass
class ProtocolState:
    """
    High-level encapsulation of the entire protocol's thermodynamic state.
    Used for mathematical divergence calculations and on-chain analytics.
    """
    pool_counter: u256
    app_counter: u256
    total_processed: u256
    total_approved: u256
    total_rejected: u256
    total_revoked: u256
    global_score_index_bps: u256

class DeFiLendingProtocol(gl.Contract):
    """
    NexusLend Enterprise DeFi Protocol.
    
    This contract handles the ingestion, translation, validation, and AI-driven 
    underwriting of cross-border multi-lingual decentralized loans. It guarantees 
    Byzantine fault tolerance by de-coupling deterministic state storage from non-deterministic LLM execution.
    """
    pools: TreeMap[str, Pool]
    applications: TreeMap[str, Application]
    pool_ids: DynArray[str]
    app_ids: DynArray[str]
    state: ProtocolState

    def __init__(self):
        """
        Initializes the state trees. TreeMaps provide O(log n) deterministic
        lookups, satisfying strict GenLayer performance constraints.
        """
        self.state.pool_counter = u256(0)
        self.state.app_counter = u256(0)
        self.state.total_processed = u256(0)
        self.state.total_approved = u256(0)
        self.state.total_rejected = u256(0)
        self.state.total_revoked = u256(0)
        self.state.global_score_index_bps = u256(0)
        self.owner = str(gl.message.sender_address)

    def _require_owner(self):
        """Idempotency and role check."""
        if str(gl.message.sender_address) != self.owner:
            raise gl.vm.UserError(f"{ERROR_EXPECTED} Unauthorized: Not the owner")

    # -------------------------------------------------------------------------
    # CORE BUSINESS LOGIC
    # -------------------------------------------------------------------------

    @gl.public.write
    def create_lending_pool(self, amount: int, conditions: str) -> str:
        """
        Registers a new capital pool with specific underwriting conditions.
        """
        self._require_owner()
        clean_conditions = _deep_sanitize(conditions)[:MAX_CONDITIONS_LEN]
        
        self.state.pool_counter = u256(int(self.state.pool_counter) + 1)
        p_id = f"POOL-{int(self.state.pool_counter)}"
        
        pool = Pool(
            pool_id=p_id,
            amount=u256(amount),
            conditions=clean_conditions,
            status="ACTIVE",
            created_at=u256(0)
        )
        self.pools[p_id] = pool
        self.pool_ids.append(p_id)
        return p_id

    @gl.public.write.payable
    def apply_for_loan(self, poolId: str, applicantPitch: str) -> str:
        """
        Ingests a new loan proposal in any language from an external client.
        """
        if poolId not in self.pools:
            raise gl.vm.UserError(f"{ERROR_EXPECTED} Pool not found")
            
        clean_pitch = _deep_sanitize(applicantPitch)[:MAX_PITCH_LEN]
        
        self.state.app_counter = u256(int(self.state.app_counter) + 1)
        a_id = f"APP-{int(self.state.app_counter)}"
        
        app = Application(
            app_id=a_id,
            pool_id=poolId,
            applicant=str(gl.message.sender_address),
            pitch=clean_pitch,
            status="PENDING_EVALUATION",
            translated_pitch="",
            score=u256(0),
            ai_notes="",
            collateral=u256(gl.message.value),
            debt=u256(0),
            created_at=u256(0), # Epoch 0
            last_updated=u256(0)
        )
        self.applications[a_id] = app
        self.app_ids.append(a_id)
        return a_id

    @gl.public.write
    def evaluate_loan_application(self, appId: str) -> bool:
        """
        Triggers the GenLayer Subjective Consensus engine to translate and underwrite.
        """
        self._require_owner()
        if appId not in self.applications:
            raise gl.vm.UserError(f"{ERROR_EXPECTED} App not found")
            
        app = self.applications[appId]
        if app.status != "PENDING_EVALUATION":
            raise gl.vm.UserError(f"{ERROR_EXPECTED} Not pending")
            
        pool = self.pools[app.pool_id]
        
        pitch = app.pitch
        conditions = pool.conditions
        
        def leader_fn_str() -> str:
            """Leader execution environment."""
            url = _extract_url(pitch)
            risk_data = "NO EXTERNAL RISK DATA."
            if url:
                try:
                    res = gl.nondet.web.get(url)
                    if res.status == 200:
                        body = res.body.decode('utf-8')[:1000]
                        lower = body.lower()
                        for marker in ["cloudflare", "ddos protection", "are you human", "captcha"]:
                            if marker in lower:
                                raise Exception(f"challenge page detected: {marker}")
                        risk_data = body
                except Exception:
                    pass
                
            prompt = _interpret_leader_prompt(conditions, pitch, risk_data)
            raw_analysis = gl.nondet.exec_prompt(prompt, response_format="json")
            if isinstance(raw_analysis, str):
                try:
                    analysis = json.loads(raw_analysis)
                except Exception:
                    analysis = {}
            else:
                analysis = raw_analysis
            return json.dumps({
                "english_translation": _clean_translation(analysis),
                "score": _parse_ratio_bps(analysis),
                "verdict": _parse_verdict(analysis),
                "notes": _clean_notes(analysis)
            })

        def validator_fn(leader_res) -> bool:
            if not isinstance(leader_res, gl.vm.Return):
                return _handle_leader_error(leader_res, leader_fn_str)
            try:
                mine = json.loads(leader_fn_str())
                leader = json.loads(leader_res.calldata)
                return (
                    isinstance(leader.get("english_translation"), str) and
                    isinstance(leader.get("score"), int) and
                    mine.get("verdict") == leader.get("verdict") and
                    isinstance(leader.get("notes"), str)
                )
            except Exception:
                return False

        decision_str = gl.vm.run_nondet(leader_fn_str, validator_fn)
        decision = json.loads(decision_str)
        
        # State transitions based on consensus outcome
        app.translated_pitch = decision["english_translation"]
        app.score = u256(decision["score"])
        app.status = decision["verdict"]
        app.ai_notes = decision["notes"]
        app.last_updated = u256(int(app.last_updated) + 1)
        
        if app.status == "APPROVE":
            loan_amount = int(app.collateral) // 2
            app.debt = u256(loan_amount + loan_amount // 20) # 5% interest
            gl.transfer(app.applicant, loan_amount)
            
        self.applications[appId] = app
        
        # Update System Metrics
        self.state.total_processed = u256(int(self.state.total_processed) + 1)
        if app.status == "APPROVE":
            self.state.total_approved = u256(int(self.state.total_approved) + 1)
        else:
            self.state.total_rejected = u256(int(self.state.total_rejected) + 1)
            
        return decision["verdict"] == "APPROVE"

    @gl.public.write.payable
    def repay_loan(self, appId: str) -> bool:
        """
        Allows a borrower to repay their debt and reclaim their collateral.
        Enforces strict authorization and exact value checking.
        """
        if appId not in self.applications:
            raise gl.vm.UserError(f"{ERROR_EXPECTED} App not found")
            
        app = self.applications[appId]
        if gl.message.sender_address != app.applicant:
            raise gl.vm.UserError(f"{ERROR_EXPECTED} Unauthorized: Not the applicant")
            
        if app.status != "APPROVE":
            raise gl.vm.UserError(f"{ERROR_EXPECTED} Loan is not in active state")
            
        debt = int(app.debt)
        if gl.message.value < debt:
            raise gl.vm.UserError(f"{ERROR_EXPECTED} Insufficient repayment. Need {debt}")
            
        # Refund collateral
        gl.transfer(app.applicant, int(app.collateral))
        
        # State transitions
        app.status = "REPAID"
        app.debt = u256(0)
        app.last_updated = u256(int(app.last_updated) + 1)
        
        self.applications[appId] = app
        self._recalculate_global_score()
        return True

    @gl.public.write
    def revoke_application(self, appId: str) -> bool:
        """
        Allows an external system or governance module to forcibly revoke 
        an application that was maliciously submitted or violated TOS.
        """
        if appId not in self.applications:
            raise gl.vm.UserError(f"{ERROR_EXPECTED} App not found")
            
        app = self.applications[appId]
        if gl.message.sender_address != app.applicant:
            raise gl.vm.UserError(f"{ERROR_EXPECTED} Unauthorized: Not the applicant")
        if app.status == "REVOKED":
            return True # Idempotent
        if app.status == "PENDING_EVALUATION":
            # Refund collateral for pending applications
            gl.transfer(app.applicant, int(app.collateral))
            
        old_status = app.status
        app.status = "REVOKED"
        app.ai_notes = "Revoked by applicant."
        app.last_updated = u256(int(app.last_updated) + 1)
        self.applications[appId] = app
        
        self.state.total_revoked = u256(int(self.state.total_revoked) + 1)
        
        if old_status == "APPROVE":
            self.state.total_approved = u256(int(self.state.total_approved) - 1)
            self._recalculate_global_score()
            
        return True

    @gl.public.view
    def get_all_pools(self) -> str:
        """Retrieves all pools for frontend."""
        out = []
        for i in range(1, int(self.state.pool_counter) + 1):
            pid = f"POOL-{i}"
            if pid not in self.pools:
                continue
            p = self.pools[pid]
            out.append({
                "pool_id": p.pool_id,
                "amount": int(p.amount),
                "conditions": p.conditions,
                "status": p.status
            })
        return json.dumps(out)

